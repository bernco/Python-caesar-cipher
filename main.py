#ask the user if they want to restart the cipher program?
#If they type 'yes' ask them for the direction/text/shift again and call the caesar() function again?
 
should_end = False
while not should_end:

  direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
  text = input("Type your message:\n").lower()
  shift = int(input("Type the shift number:\n"))

  #if the user enters a shift that is greater than the number of letters in the alphabet?
  shift = shift % 25

  caesar(start_text=text, shift_amount=shift, cipher_direction=direction)

  restart = input("Type 'yes' if you want to go again. Otherwise type 'no'.\n")
  if restart == "no":
    should_end = True
    print("Goodbye")
    


